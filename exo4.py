liste_de_nombres = [3, 4, 5, 6, 7, 56, 43, 45, 90, 78]

somme = sum(liste_de_nombres)
moyenne = somme / len(liste_de_nombres)

maxi = max(liste_de_nombres)
mini = min(liste_de_nombres)


print("La moyenne des nombres de la liste est :", moyenne)
print("Le plus grand nombre de la liste est :", maxi)
print("Le plus petit nombre dans la liste est :", mini)
